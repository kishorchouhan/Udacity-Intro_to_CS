def print_abacus(value):
    pattern = '00000*****'
    p = str(value)
    t = len(p)
    i = 0
    while i < (10-t):
        i += 1
        p = '0' + p
    a = 0
    while a < 10:
        print (('|' + pattern[:(10 - int(p[a]))]) + '   ' + pattern[(10 - int(p[a])):] + '|')
        a += 1

